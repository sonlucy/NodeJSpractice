/*
시퀄라이즈(Sequelize)
- Node.js 환경에서 사용할 수 있는 promise 기반의 ORM(Object-Relational Mapping) 라이브러리
- MySQL, MariaDB, SQLite, PostgreSQL, MSSQL 등 다양한 데이터베이스를 지원

시퀄라이즈 설치
- npm install sequelize sequelize-cli mysql2
- npz sequelize init
- config: DB 연결 정보를 저장하는 파일 
- models: 테이블과 모델을 정의하는 파일
- migrations: 마이그레이션 파일을 생성하고 데이터베이스 변화하는 과정들을 추척해나가는 정보로,
                실제 데이터베이스에 반영할 수도 있고 변화를 취소할 수도 있다.
                
*/

